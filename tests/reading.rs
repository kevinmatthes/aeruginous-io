use aeruginous_io::ReadPathBufLike;

#[test]
fn read_path_buf_like_failure_directory() {
    assert!(".github/".read().is_err());
}

#[test]
fn read_path_buf_like_failure_does_not_exist() {
    assert!("no_such_file.txt".read().is_err());
}

